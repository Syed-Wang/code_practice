# 字符串操作：转换家族
# 应用场景：将用户输入的字符串转换为大写字母，小写字母，大小写字母转换，首字母大写

# upper() 将字符串中的小写字母转换为大写字母
# lower() 将字符串中的大写字母转换为小写字母
# swapcase() 将字符串中的大小写字母转换为相反的大小写字母
# title() 将字符串中的每个单词的首字母转换为大写字母
# capitalize() 将字符串中的首字母转换为大写字母

s = 'hello world'
print(s.upper())
print(s.lower())
print(s.swapcase())
print(s.title())
print(s.capitalize())

s1 = s.upper()
print(s1)
